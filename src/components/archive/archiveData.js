// Helper to get counts per category
export const getCategoryCounts = () => {
  const counts = {};
  categories.forEach(cat => {
    if (cat.id === 'all') {
      counts[cat.id] = activations.length;
    } else {
      counts[cat.id] = activations.filter(a => a.category === cat.id).length;
    }
  });
  return counts;
};
